var cart = [];
function and_to_cart() {
    var pass = true;
    for (let i = 0; i < cart.length; i++) {
        if(productindex == cart[i].index) {
            console.log('found same product')
            cart[i].count++;
            pass = false;
        }
    }
    if(pass) {
        var obj = {
            index: productindex,
            id: product[productindex].id,
            name: product[productindex].name,
            price: product[productindex].price,
            img: product[productindex].img,
            count: 1
        };
        //console.log(obj)
        cart.push(obj)
    }
    console.log(cart)

    Swal.fire({
        icon: 'success',
        title: 'Add ' + product[productindex].name + ' to cart !'
    })
    $("#cartcount").css('display', 'flex').text(cart.length)
}

function opencart() {
    $("#modalCart").css('display', 'flex')
    rendercart();
}

function rendercart() {
    if(cart.length > 0) {
        var html = ''
        for ( let i = 0; i < cart.length; i++) {
            html += `<div class="carlist-items">
                        <div class="cartlist-lift">
                            <img src="${cart[i].img}">
                            <div class="carlist-detail">
                                <p class="modal-font-product-name">${cart[i].name}</p>
                                <p class="modal-font-product-price">${number_commas(cart[i].price)} THB</p>
                            </div>
                        </div>
                        <div class="cartlist-right">
                            <p onclick="deinitems('-', ${i})" class="btnc">-</p>
                            <p id="countitems${i}" style="margin:0 15px">${cart[i].count}</p>
                            <p onclick="deinitems('+', ${i})" class="btnc">+</p>
                        </div>
                    </div>    `;
        }
        $("#mycart").html(html)
    }
        
    else {
        $("#mycart").html(`<p>Not found product list</p>`)
    }
}

function deinitems(action,index) {
    if(action == '-') {
        if(cart[index].count > 0) {
            cart[index].count--;
            $("#countitems"+index).text(cart[index].count)

            if(cart[index].count <= 0) {
                Swal.fire({
                    icon: 'warning',
                    title: 'Are you sure to delete?',
                    showConfirmButton: true,
                    showCancelButton: true,
                    confirmButtonText: 'Yes, delete it!',
                    cancelButtonText: 'No, cancel!'
                }).then((res) => {
                    if(res.isConfirmed) {
                        cart.splice(index,1)
                        console.log(cart)
                        rendercart();
                        $("#cartcount").css('display', 'flex').text(cart.length)

                        if(cart.length <= 0) {
                            $("#cartcount").css('display','none')
                        }
                    }
                    else {
                        cart[index].count++;
                        $("#countitems"+index).text(cart[index].count)
                    }
                })
            }
        }
    }
    else if(action == '+') {
        cart[index].count++;
        $("#countitems"+index).text(cart[index].count)
    }
}

